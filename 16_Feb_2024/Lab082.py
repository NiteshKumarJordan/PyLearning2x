# few functions of set- union, intersection, differences

# Union

set1 = {1, 2, 3,4,5}
set2 = {4, 5, 6,7,8}
print(set1.union(set2))

# Intersection
my_set = set1.intersection(set2)
print(my_set)

# Difference
point = set1.difference(set2)
print(point)




