# For Loop
# Repetation of block of codes

# print hello world 5 times
for i in range(5):
    print(i)
print("----------------------------------------")

# second iteration-it will print from 1 to 4
for i in range(1,5):
    print(i)
print("----------------------------------------")

# third iteration-it will print with gap between the numbers
for i in range(1,5,2):
    print(i)

